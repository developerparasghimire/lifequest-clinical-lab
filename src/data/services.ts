/* ──────────────────────────────────────────────────────────────────────────
   Heuristic categoriser. Runs once at module load.
   ────────────────────────────────────────────────────────────────────────── */
function classify(name: string, sample: string, method: string): ServiceCategory {
  const n = name.toUpperCase();
  const s = sample.toUpperCase();
  const m = method.toUpperCase();

  if (/HER2|CA\s?125|CA\s?15|CA\s?19|CA\s?72|CEA|AFP|PSA|CYFRA|BRCA|TUMOR|CALCITONIN|BETA HCG|HCG|PIVKA|BCR\/ABL/.test(n)) return "tumor";
  if (/HISTOPAT|BIOPSY|FNAC|CYTOLOGY|IHC|HER2.*IHC|PAP SMEAR|LBC|CELL BLOCK|PARAFFIN|KARYOTYP|FISH|PDL|KI 67|ER \(|PR \(|ER\/PR/.test(n) || /PARAFFIN|FNA|SMEAR/.test(s)) return "histopathology";
  if (/PCR|NGS|MUTATION|GENE|GENOTYP|FISH|MICROARRAY|DNA TEST|CAG REPEAT|EXOME|HPV|JAK2|BRAF|EGFR|HUNTINGTON|CHROMO/.test(n) || /PCR|NGS/.test(m)) return "molecular";
  if (/CULTURE|C\/S|GRAM|KOH|AFB|GIEMSA|FUNGAL|BACTER|FITE|HANGING DROP|INDIA INK|MANTOUX|TB |GENE Xpert|SPUTUM|SWAB/.test(n) || /CULTURE|MICROSCOPY/.test(m) && /SWAB|SPUTUM/.test(s)) return "microbiology";
  if (/HIV|HBS|HCV|HEV|HAV|HBE|HBC|DENGUE|MALARIA|TYPHOID|WIDAL|BRUCELLA|LEPTO|FILARIA|TPHA|VDRL|SCRUB|RUBELLA|TOXO|CMV|EBV|HSV|VZV|VARICELLA|HEPATITIS|COVID|INFLUENZA|H1N1|MEASEL|MUMPS|CHLAMYDIA|GONORRHEA|H\.PYLORI|H\. PYLORI|HPV|JEV|PARVO|CRYPTO|SCRUB|CYSTECER|LYME|LEGION|STREPTO|ECHINOCOC|ENTAMOEBA|HYDATID|MICROFILARIA|NEUROCYS|ASPERGILLUS|GALACTOMAN|PNEUMOCYS|TB |TUBERC|MANTOUX|PCP|CRYPTOCOC/.test(n)) return "infectious";
  if (/ANTIBOD|ANTIGEN|ANA|ANCA|APLA|DS DNA|ENA|HLA|RF|ASO|CCP|TTG|IgG|IgM|IgA|IgE|IGG|IGM|IGA|IGE|COMPLEMENT|C3|C4|CARDIOLIPIN|GLYCOPROTEIN|MYELO|SCL-70|SS-A|SS-B|MOG|NMO|GBM|MPO|PR3|GAD|MUSK|NMDA|TPO|THYROGLOBUL|INTRINSIC|ISLET|PARIETAL|AMA|ASMA|LKM|LC-1|CENTROMERE|CELIAC|GANGLIOSID|VASCULIT|PARANEOPLAS|PHOSPHO|AUTOIMMUN|EPIDERM|PEMPHIG|CRYPTO COCC ANTIGEN/.test(n)) return "immunology";
  if (/FSH|LH|PROLACTIN|TESTOSTER|ESTRA|ESTROG|PROGESTER|CORTISOL|ACTH|TSH|FT3|FT4|THYROID|GROWTH HORMONE|INSULIN|C PEPTIDE|ALDOSTER|RENIN|PARATHY|PTH|DHEA|DHT|ANDROGEN|ANDROST|17-OH|AMH|INHIBIN|GASTRIN|HOMA|SHBG|SEX HORMON|HORMONE|IGF|ERYTHROPOET|METANEPHRI|CATECHOLAMI|NORMETANEP|VMA/.test(n)) return "endocrine";
  if (/CBC|HEMOGLOB|ESR|PBS|RETICULOC|BLEEDING|CLOTTING|COOMB|BLOOD GROUP|PT|APTT|PROTHROMB|D-DIMER|D - DIMER|FDP|FIBRINOG|FACTOR|PROTEIN C|PROTEIN S|ANTI THROMB|THROMBO|LUPUS ANTI|G6PD|SICKLING|OSMOTIC|HBA1C|ABSOLUTE EOSIN|PNH|VON WILL|HAPTO|EOSINOPHIL/.test(n)) return "hematology";
  if (/CARDIAC|CK|CPK|TROPONIN|NT-PRO|BNP|HOMOCYSTEIN|APOLIPOPROT|LIPOPROTEIN|LIPID|CHOLESTER|LDL|HDL|TRIGLY/.test(n)) return "cardiac";
  if (/VITAMIN|FOLIC|B12|FERRITIN|IRON|TIBC|ZINC|COPPER|CALCIUM|MAGNES|PHOSPH|SELENIUM|CARNITIN|METHYLMAL/.test(n)) return "vitamins";
  if (/URIN|MICROALBUM|24 HRS URI|24 HR URINE|24 HOUR URI|SPOT URIN/.test(n) || /URINE/.test(s)) return "urine";
  if (/CSF|FLUID|ASCITIC|PLEURAL|SYNOVIAL|BAL FLUID|BODY FLUID|HYDATID FLUID|AMNIOTIC/.test(n) || /CSF|FLUID/.test(s)) return "fluid";
  if (/GLUCOSE|ALBUM|GLOBUL|TOTAL PROTEIN|UREA|CREATIN|URIC ACID|BILIRUB|ALP|ALT|AST|SGOT|SGPT|GGT|GAMMA|ELECTROLYT|SODIUM|POTASSIUM|CHLORIDE|BICARB|AMYLASE|LIPASE|LDH|ACID PHOSPHAT|A\/G|RENAL FUNCTION|LIVER FUNCTION|GFR|BUN|OSMOL|FRACTIONAL|LACTATE|BILE ACID|ACE/.test(n)) return "biochemistry";

  return "general";
}

